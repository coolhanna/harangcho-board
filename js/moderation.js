    "시발","씨발","씨빨","시바","씨바","싯팔","시팔","씨팔","슈발","쓔발","십발","씨발라","좆","좃","조까","존나","존내","좆나","개새","개색","새끼","새기","쉐끼","썅","쌍놈","쌍년",
    "병신","븅신","빙신","등신","지랄","지럴","이지랄","개지랄","꼴깝","닥쳐","닥치","꺼져","꺼저","엿먹","엿이나","미친놈","미친년","또라이","돌아이","호로","후레","개년","개놈","개차반",
    "창녀","걸레","보지","자지","좆같","좃같","니미","니애미","느금","느검","엠창","앰창","애미","tlfqkf","섹스","야동","자위","꼬추","불알","꼴린","싸버","싸질","닥치라",
